package com.example.assignment2;

public class Food {
    private Integer foodId;
    private String foodName;
    private String category;
    private Integer calorieAmount;
    private String servingUnit;
    private Double servingAmount;
    private Integer fat;

    public Food(String foodName, String category, Integer calorieAmount, String servingUnit, Double servingAmount, Integer fat) {
        this.foodName = foodName;
        this.category = category;
        this.calorieAmount = calorieAmount;
        this.servingUnit = servingUnit;
        this.servingAmount = servingAmount;
        this.fat = fat;
    }

    public Food(){

    }

    public Integer getFoodId() {
        return foodId;
    }

    public void setFoodId(Integer foodId) {
        this.foodId = foodId;
    }

    public String getFoodName() {
        return foodName;
    }

    public void setFoodName(String foodName) {
        this.foodName = foodName;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public Integer getCalorieAmount() {
        return calorieAmount;
    }

    public void setCalorieAmount(Integer calorieAmount) {
        this.calorieAmount = calorieAmount;
    }

    public String getServingUnit() {
        return servingUnit;
    }

    public void setServingUnit(String servingUnit) {
        this.servingUnit = servingUnit;
    }

    public Double getServingAmount() {
        return servingAmount;
    }

    public void setServingAmount(Double servingAmount) {
        this.servingAmount = servingAmount;
    }

    public Integer getFat() {
        return fat;
    }

    public void setFat(Integer fat) {
        this.fat = fat;
    }
}
